#include<stdio.h>
#include<stdlib.h>
#include<iostream>
using namespace std;
main()
{
	/**********  Ciclo for()  **********/
	
	printf("Ciclo for()\n");
	
	for(int i=0; i<=4; i++)
	{
		printf("Hola\n");
	}
	
	/**********************************/
	
	printf("\n\n");
	
	/**********  Ciclo while()  **********/
	
	printf("Ciclo while()\n");
	
	int j=1;
	while(j<6)
	{
		printf("Hola\n");
		j++;
	}
	
	/**********************************/
	
  cout << endl;
  system("Pause");
}
